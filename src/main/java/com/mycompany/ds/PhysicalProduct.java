public abstract class PhysicalProduct extends Product{
    private double weight;

    public PhysicalProduct(double weight, String name, double price) {
        super(name, price);
        this.weight = weight;
    }

    public double getWeight() {
        return weight;
    }

    public void setWeight(double weight) {
        this.weight = weight;
    }
}
